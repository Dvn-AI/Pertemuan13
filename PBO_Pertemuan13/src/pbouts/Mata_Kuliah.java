    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        EntityManagerFactory emf = Persistence.createEntityManagerFactory("utspbo1PU");
        EntityManager em = emf.createEntityManager();
        em.getTransaction().begin();

        String Kodemk = txtKMK.getText();
        MataKuliah PBO = em.find(MataKuliah.class, Kodemk);

        if (PBO != null) {
            PBO.setSks(txtSKS.getText());
            PBO.setNamamk(txtNMK.getText());
            PBO.setSemesterajar(txtSA.getText());
            em.getTransaction().commit();
        } else {
            System.out.println("Data kosong atau tidak ada");
        }

        em.close();
        JOptionPane.showMessageDialog(null, "Berhasil Update Data!");
        tampil();
    }//GEN-LAST:event_btnUpdateActionPerformed

