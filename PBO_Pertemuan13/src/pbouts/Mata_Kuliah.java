    private void btnUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadActionPerformed
        // TODO add your handling code here:
        JFileChooser jfc = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        int returnValue = jfc.showOpenDialog(null);
        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File filePilihan = jfc.getSelectedFile();
            System.out.println("yang dipilih : " + filePilihan.getAbsolutePath());
            try (BufferedReader br = new BufferedReader(new FileReader(filePilihan))) {
                Class.forName(driver);
                con = DriverManager.getConnection(koneksi, user, password);
                String baris;
                String pemisah = ";";
                while ((baris = br.readLine()) != null) {
                    String[] dataMhs = baris.split(pemisah);
                    String KMK = dataMhs[0];
                    String SKS = dataMhs[1];
                    String NMK = dataMhs[2];
                    String SA = dataMhs[3];
                    if (!KMK.isEmpty() && !SKS.isEmpty() && !NMK.isEmpty() && !SA.isEmpty()) {
                        String sql = "INSERT INTO MATA_KULIAH (KodeMK, SKS, namaMK, SemesterAjar) VALUES (?,?,?,?)";
                        pst = con.prepareStatement(sql);
                        pst.setString(1, KMK);
                        pst.setString(2, SKS);
                        pst.setString(3, NMK);
                        pst.setString(4, SA);
                        pst.executeUpdate();

                        JOptionPane.showMessageDialog(null, "Done");
                    } else {
                        JOptionPane.showMessageDialog(null, "Failed");
                    }
                }

            } catch (FileNotFoundException ex) {
                java.util.logging.Logger.getLogger(Mata_Kuliah.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            } catch (IOException | SQLException | ClassNotFoundException ex) {
                java.util.logging.Logger.getLogger(Mata_Kuliah.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
            } finally {

                try {
                    if (pst != null) {
                        pst.close();
                    }
                    if (con != null) {
                        con.close();
                    }
                } catch (SQLException ex) {
                    java.util.logging.Logger.getLogger(Mata_Kuliah.class.getName()).log(java.util.logging.Level.SEVERE, null, ex);
                }
            }
        }
        tampil();
    }//GEN-LAST:event_btnUploadActionPerformed

